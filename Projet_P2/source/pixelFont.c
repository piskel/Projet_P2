const bool* pixelFont_AsciiToFont(char letter)
	{
	if(letter >= 'a' && letter <= 'z')
		{
			return pixFont[letter-'a'];
		}
	else if(letter >= 'A' && letter <= 'Z')
		{
			return pixFont[letter-'A'];
		}
	else if(letter >= '0' && letter <= '9')
		{

		return pixFont[letter-'0'+26];
		}
	else if(letter == ' ')
		{
		return pixFont[35];
		}
	else
		{
		return pixFont[36];
		}
	}

